package com.company;

public class class_number {
    public static int Max(int a, int b) {
        if(a>b){
            return a;
        }else{
            return b;
        }
    }

    public int Min(int a, int b){
        if(a>b){
            return b;
        }else{
            return a;
        }
    }
}
